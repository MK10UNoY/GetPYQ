    fun getSubjectList(branch: String, semester: Int): List<String> {
        val key = if (semester in 1..2) "Al$semester" else "${branch.uppercase()}$semester"
        return SubjectListProvider::class.members
            .find { it.name == key }
            ?.call(SubjectListProvider) as? List<String> ?: emptyList()
    }
